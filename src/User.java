/**
 * Clasa User este folosita pentru a retine datele utilizatorului
 */
public class User {
    public String email;
    public String parola;

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getParola() {
        return parola;
    }

    public void setParola(String parola) {
        this.parola = parola;
    }
}
